#!/usr/bin/env python3
"""
Statistical validity analysis of LoCoMo per-category scores.

Computes Wilson Score 95% confidence intervals for each system's per-category
accuracy, showing that the significantly imbalanced category sizes make
per-category comparisons statistically indistinguishable for closely-ranked systems.

Reads from: per_category_breakdown.json (generated by audit_results.py)
Outputs: STATISTICAL_VALIDITY.md

No external dependencies — uses only Python stdlib.
"""

import json
import math
from pathlib import Path

Z = 1.96  # 95% confidence


def wilson_ci(n: int, k: int) -> tuple[float, float, float]:
    """
    Wilson Score confidence interval for a binomial proportion.

    More accurate than the normal (Wald) approximation, especially for
    proportions near 0 or 1 and small sample sizes.

    Args:
        n: Number of trials (questions in category)
        k: Number of successes (correct answers)

    Returns:
        (lower, upper, width) — all as proportions (0-1 scale)
    """
    if n == 0:
        return 0.0, 0.0, 0.0

    p_hat = k / n
    denom = 1 + Z**2 / n
    center = (p_hat + Z**2 / (2 * n)) / denom
    spread = Z * math.sqrt((p_hat * (1 - p_hat) + Z**2 / (4 * n)) / n) / denom
    lower = max(0.0, center - spread)
    upper = min(1.0, center + spread)
    return lower, upper, upper - lower


# Category metadata: category_id -> (name, total_questions)
# Ordered by descending n for consistent table presentation
CATEGORIES = {
    "cat_4_single_hop": ("Single-hop", 841),
    "cat_2_temporal": ("Temporal", 321),
    "cat_1_multi_hop": ("Multi-hop", 282),
    "cat_3_open_domain": ("Open-domain", 96),
}

# Display names for systems
SYSTEM_NAMES = {
    "evermemos": "EverMemOS",
    "mem0": "Mem0",
    "memos": "MemOS",
    "memu": "MemU",
    "zep": "Zep",
}


def load_data() -> dict:
    path = Path(__file__).parent / "per_category_breakdown.json"
    with open(path) as f:
        return json.load(f)


def generate_report(data: dict) -> str:
    lines = []
    w = lines.append

    w("# Statistical Validity of LoCoMo Per-Category Scores")
    w("")
    w("## The Problem")
    w("")
    w("LoCoMo's per-category breakdowns are treated as meaningful signal by every")
    w("system that publishes results on this benchmark. Teams compare their Single-hop")
    w("vs. Multi-hop vs. Temporal vs. Open-domain scores to identify strengths and")
    w("weaknesses. Leaderboards rank systems per-category.")
    w("")
    w("But the category sample sizes are significantly imbalanced:")
    w("")
    w("| Category | n | % of Dataset | Status |")
    w("|----------|--:|-----------:|--------|")
    w("| Single-hop | 841 | 42.3% | Evaluated |")
    w("| **Adversarial** | **446** | **22.5%** | **Excluded** ([why](../methodology/discrepancies.md#category-5-exclusion)) |")
    w("| Temporal | 321 | 16.2% | Evaluated |")
    w("| Multi-hop | 282 | 14.2% | Evaluated |")
    w("| Open-domain | 96 | 4.8% | Evaluated |")
    w(f"| **Total in dataset** | **1,986** | **100%** | |")
    w(f"| **Total evaluated** | **1,540** | **77.5%** | |")
    w("")
    w(f"The largest evaluated category (Single-hop) has **{841/96:.1f}x** more questions")
    w(f"than the smallest (Open-domain). The second-largest category in the entire")
    w(f"dataset (Adversarial, n=446) is excluded from all published evaluations because")
    w(f"444 of its 446 questions have no ground truth `answer` field — only an")
    w(f"`adversarial_answer` (the deliberately wrong answer). You cannot evaluate a")
    w(f"system against an answer key that doesn't exist. This means 22.5% of the")
    w(f"dataset — more questions than Multi-hop, Temporal, or Open-domain")
    w(f"individually — is discarded before evaluation begins.")
    w("")
    w(f"The imbalance among the four evaluated categories means the statistical")
    w(f"precision of per-category scores varies substantially. A 5-point difference in")
    w(f"Single-hop (n=841) may be statistically significant. A 5-point difference in")
    w(f"Open-domain (n=96) falls well within the confidence interval.")
    w("")
    w("## Confidence Intervals")
    w("")
    w("We use the [Wilson Score interval](https://en.wikipedia.org/wiki/Binomial_proportion_confidence_interval#Wilson_score_interval)")
    w("to compute 95% confidence intervals for each per-category accuracy. Wilson")
    w("Score is preferred over the normal (Wald) approximation because it is more")
    w("accurate for proportions near 0 or 1 and for smaller sample sizes.")
    w("")
    w("**Important:** Confidence interval width is not fixed — it depends on both the")
    w("sample size *and* the observed proportion. A system scoring 50% has a wider CI")
    w("than one scoring 90% on the same number of questions, because the binomial")
    w("variance p(1-p) peaks at p=0.5. The tables below show the actual CI for each")
    w("system's actual score, not a one-size-fits-all width.")
    w("")

    # Per-system tables
    for sys_key in ["evermemos", "zep", "memos", "memu", "mem0"]:
        sys_data = data[sys_key]
        sys_name = SYSTEM_NAMES[sys_key]
        w(f"### {sys_name}")
        w("")
        w(f"| Category | n | Score | 95% CI | Width |")
        w(f"|----------|--:|------:|-------:|------:|")

        for cat_key, (cat_name, n) in CATEGORIES.items():
            cat_data = sys_data["per_category"][cat_key]
            k = cat_data["majority_correct"]
            p = k / n
            lo, hi, width = wilson_ci(n, k)
            w(f"| {cat_name} | {n} | {p*100:.1f}% | {lo*100:.1f}% – {hi*100:.1f}% | {width*100:.1f} pts |")

        # Overall
        overall = sys_data["overall"]
        k_all = overall["majority_correct"]
        lo_all, hi_all, w_all = wilson_ci(1540, k_all)
        w(f"| **Overall** | **1,540** | **{k_all/1540*100:.1f}%** | **{lo_all*100:.1f}% – {hi_all*100:.1f}%** | **{w_all*100:.1f} pts** |")
        w("")

    # Pairwise distinguishability
    w("## Can We Distinguish Systems Per-Category?")
    w("")
    w("Two systems are statistically distinguishable at the 95% level if their")
    w("confidence intervals do not overlap. The table below tests all adjacent")
    w("pairs on the overall leaderboard (EverMemOS > Zep > MemOS > MemU > Mem0)")
    w("within each category.")
    w("")

    pairs = [
        ("evermemos", "zep"),
        ("zep", "memos"),
        ("memos", "memu"),
        ("memu", "mem0"),
    ]

    w("| Category | Pair | Scores | Gap | CIs Overlap? | Distinguishable? |")
    w("|----------|------|-------:|----:|:-------------|:-----------------|")

    for cat_key, (cat_name, n) in CATEGORIES.items():
        for sys_a, sys_b in pairs:
            cat_a = data[sys_a]["per_category"][cat_key]
            cat_b = data[sys_b]["per_category"][cat_key]
            ka = cat_a["majority_correct"]
            kb = cat_b["majority_correct"]
            pa = ka / n
            pb = kb / n
            lo_a, hi_a, _ = wilson_ci(n, ka)
            lo_b, hi_b, _ = wilson_ci(n, kb)

            gap = abs(pa - pb) * 100
            overlap = hi_b > lo_a and hi_a > lo_b
            dist = "NO" if overlap else "YES"

            name_a = SYSTEM_NAMES[sys_a]
            name_b = SYSTEM_NAMES[sys_b]
            w(f"| {cat_name} | {name_a} vs {name_b} | "
              f"{pa*100:.1f}% vs {pb*100:.1f}% | {gap:.1f} pp | "
              f"{'Yes' if overlap else 'No'} | **{dist}** |")
    w("")

    # Count distinguishable
    total_pairs = 0
    dist_pairs = 0
    for cat_key, (cat_name, n) in CATEGORIES.items():
        for sys_a, sys_b in pairs:
            cat_a = data[sys_a]["per_category"][cat_key]
            cat_b = data[sys_b]["per_category"][cat_key]
            ka = cat_a["majority_correct"]
            kb = cat_b["majority_correct"]
            lo_a, hi_a, _ = wilson_ci(n, ka)
            lo_b, hi_b, _ = wilson_ci(n, kb)
            overlap = hi_b > lo_a and hi_a > lo_b
            total_pairs += 1
            if not overlap:
                dist_pairs += 1

    w(f"**{dist_pairs} of {total_pairs}** adjacent-pair comparisons ({dist_pairs/total_pairs*100:.0f}%) are")
    w(f"statistically distinguishable at the 95% level. The rest are indistinguishable")
    w(f"— the gap between systems is smaller than the measurement uncertainty.")
    w("")

    # Minimum distinguishable gap
    w("## Minimum Distinguishable Gap Per Category")
    w("")
    w("How far apart must two systems score to be reliably distinguished? This depends")
    w("on both systems' scores, but as a practical guide, the table below shows the")
    w("CI width at representative score levels. Two systems need a gap of *at least*")
    w("one full CI width to have non-overlapping intervals.")
    w("")
    w("| Category | n | Status | CI Width at 75% | CI Width at 85% | CI Width at 95% |")
    w("|----------|--:|--------|----------------:|----------------:|----------------:|")
    all_cats_for_gap = [
        ("Single-hop", 841, "Evaluated"),
        ("Adversarial", 446, "**Excluded**"),
        ("Temporal", 321, "Evaluated"),
        ("Multi-hop", 282, "Evaluated"),
        ("Open-domain", 96, "Evaluated"),
    ]
    for cat_name_g, n_g, status in all_cats_for_gap:
        k75 = round(n_g * 0.75)
        k85 = round(n_g * 0.85)
        k95 = round(n_g * 0.95)
        _, _, w75 = wilson_ci(n_g, k75)
        _, _, w85 = wilson_ci(n_g, k85)
        _, _, w95 = wilson_ci(n_g, k95)
        w(f"| {cat_name_g} | {n_g} | {status} | {w75*100:.1f} pts | {w85*100:.1f} pts | {w95*100:.1f} pts |")
    w("")
    w("For Open-domain (n=96), systems must differ by **14-17 percentage points** to")
    w("be statistically distinguishable. For Multi-hop (n=282), the threshold is")
    w("**8-10 points**. Even Single-hop (n=841) requires a **3-5 point** gap.")
    w("")
    w("Note: Adversarial (n=446) is included for context — it *would* have had")
    w("reasonable precision (6-8 pts) if it were usable, making it the")
    w("second-best category for statistical power. Instead it is excluded entirely.")
    w("")
    w("Most published system comparisons on LoCoMo fall well within these margins.")
    w("")

    # The "why this matters" section
    w("## Why This Matters")
    w("")
    w("The per-category breakdowns are the most-cited part of LoCoMo results. Teams")
    w("use them to claim their system is \"strong on temporal reasoning\" or \"weak on")
    w("multi-hop.\" But with confidence intervals this wide, those claims are not")
    w("supported by the data:")
    w("")
    w("1. **Open-domain (n=96) has CI widths of 14-20 points,** which exceeds the")
    w("   score gap between most systems in this category. Per-category comparisons")
    w("   should note this limitation.")
    w("")
    w("2. **Multi-hop and Temporal comparisons require large gaps.** At n=282 and")
    w("   n=321 respectively, 5-point differences fall within the CI. An 8-10 point gap is")
    w("   the minimum threshold for a statistically meaningful difference.")
    w("")
    w("3. **Only Single-hop has reasonable precision.** With n=841, the CI widths are")
    w("   3-6 points. This is the only category where moderate differences (5+")
    w("   points) between systems are statistically meaningful.")
    w("")
    w("4. **The 8.8x sample size ratio between Single-hop and Open-domain results in")
    w("   correspondingly different statistical precision.** CI width scales as")
    w("   1/sqrt(n), so Open-domain measurements are inherently ~3x less precise.")
    w("   If all four categories are important enough to measure and report")
    w("   separately, all four would ideally have sample sizes sufficient for")
    w("   meaningful comparison.")
    w("")
    w("5. **These CI issues compound with the ground truth errors.** The 99 corrupted")
    w("   answers documented in [AUDIT_REPORT.md](../AUDIT_REPORT.md) are not")
    w("   distributed uniformly across categories. Open-domain has 9 errors in 96")
    w("   questions (9.4% error rate) vs. Single-hop's 36 errors in 841 questions")
    w("   (4.3%). The smallest category has the highest error rate *and* the widest")
    w("   confidence intervals — a double penalty on measurement quality.")
    w("")

    # Run methodology section
    w("## Do Multiple Evaluation Runs Fix This?")
    w("")
    w("A natural response to wide confidence intervals is to run the evaluation")
    w("multiple times. But there are two fundamentally different kinds of \"multiple")
    w("runs,\" and the community conflates them:")
    w("")
    w("**Judge reruns** score the *same* system outputs multiple times with the LLM")
    w("judge. This measures judge consistency (inter-rater reliability). If a system")
    w("produces answer X to question #47, re-judging that same answer 3 times tells")
    w("you how noisy the judge is — not how confident you should be in the system's")
    w("score. The sample size is still n. Judge reruns do not shrink confidence")
    w("intervals.")
    w("")
    w("**End-to-end reruns** re-run the entire pipeline: retrieval, generation, and")
    w("judging. Each run produces potentially different answers because LLM generation")
    w("is stochastic (temperature > 0) and retrieval may vary. This generates genuinely")
    w("new data. In theory, k independent end-to-end runs of n questions yield up to")
    w("n×k effective observations, shrinking the CI by up to √k.")
    w("")
    w("### Who Does What")
    w("")
    w("| System | Reported Runs | Type | Variance Reported | Source |")
    w("|--------|:---:|------|:---:|--------|")
    w("| Mem0 | 10 | End-to-end | Yes (mean ± std) | [Paper](https://arxiv.org/abs/2504.19413) |")
    w("| Zep | 1 (original) | — | No (original) | [Issue #5](https://github.com/getzep/zep-papers/issues/5) |")
    w("| EverMemOS | Not specified | — | No | [Eval README](https://github.com/EverMind-AI/EverMemOS) |")
    w("| MemU | Not specified | — | No | — |")
    w("| LoCoMo baselines | Not specified | — | No | [Maharana et al.](https://arxiv.org/abs/2402.17753) |")
    w("")
    w("Only Mem0 documents a rigorous multi-run methodology. Zep's original 84%")
    w("claim was based on a single run (and separately inflated by including")
    w("Category 5 — see [discrepancies](../methodology/discrepancies.md)).")
    w("EverMemOS, MemU, and the original LoCoMo baselines report point estimates")
    w("with no variance, which in practice means single-run results.")
    w("")
    w("### Why Multiple Runs Don't Fix the Imbalance")
    w("")
    w("Even in the best case — fully independent end-to-end reruns — uniform runs")
    w("across all categories cannot fix the sample size asymmetry. The table below")
    w("shows CI widths at a representative 75% accuracy with 10 uniform runs:")
    w("")
    w("| Category | n | n × 10 | 1-Run CI Width | 10-Run CI Width | Precision Ratio |")
    w("|----------|--:|-------:|---------------:|----------------:|:---:|")
    for cat_name_r, n_r in [("Single-hop", 841), ("Temporal", 321),
                             ("Multi-hop", 282), ("Open-domain", 96)]:
        k75_1 = round(n_r * 0.75)
        _, _, w1 = wilson_ci(n_r, k75_1)
        k75_10 = round(n_r * 10 * 0.75)
        _, _, w10 = wilson_ci(n_r * 10, k75_10)
        k75_sh10 = round(841 * 10 * 0.75)
        _, _, w_sh10 = wilson_ci(841 * 10, k75_sh10)
        ratio = f"{w10/w_sh10:.1f}x"
        w(f"| {cat_name_r} | {n_r} | {n_r*10:,} | {w1*100:.1f} pts | "
          f"{w10*100:.1f} pts | {ratio} |")
    w("")
    w("Ten runs shrink every CI proportionally, but Open-domain (5.5 pts) is still")
    w("**3.0x less precise** than Single-hop (1.9 pts). The 8.8x sample size ratio")
    w("is baked into the dataset. To equalize Open-domain with Single-hop at 10 runs,")
    w("you would need ~88 runs of Open-domain alone — impractical for most")
    w("evaluation budgets.")
    w("")
    w("### Stochasticity and the LLM-as-Judge Trade-off")
    w("")
    w("LLM-as-judge is one of the most widely adopted scalable evaluation methods for")
    w("open-ended memory recall — exact match and F1 do not work across different")
    w("model phrasings. The stochasticity this introduces is an inherent property")
    w("of the evaluation approach, not a flaw per se. That said, it has implications")
    w("for how multiple runs reduce uncertainty.")
    w("")
    w("The √k improvement assumes each run is fully independent. In practice, memory")
    w("systems are partially deterministic: most questions are answered the same way")
    w("every time (always right or always wrong), with only a fraction of \"edge\"")
    w("questions varying between runs. A question the system always gets right")
    w("(p ≈ 1.0) contributes zero new information from additional runs because its")
    w("per-question variance p(1−p) ≈ 0.")
    w("")
    w("For a realistic system where ~70% of questions are near-deterministic, the")
    w("effective sample size from 10 runs falls well short of the theoretical n×10.")
    w("The exact benefit depends on the system's stochasticity profile, which is")
    w("rarely reported. Mem0's published standard deviations range from ±0.11 to")
    w("±0.67 percentage points across 10 runs (Table 1, [arXiv:2504.19413](https://arxiv.org/abs/2504.19413)).")
    w("Even the highest-variance category (Multi-hop, ±0.67) represents modest")
    w("run-to-run variation, suggesting that most questions produce the same answer")
    w("every time and the 10 runs provide well under 10× the information.")
    w("")
    w("### The Paradox")
    w("")
    w("Multiple runs are the right idea applied to the wrong problem. They reduce")
    w("*measurement noise* (judge variance, generation stochasticity) but cannot fix")
    w("*design noise* (too few questions in a category). The fundamental issue is that")
    w("96 questions is insufficient for meaningful comparison regardless of how many")
    w("times you score them. Fixing this requires more questions, not more runs.")
    w("")

    w("## Methodology")
    w("")
    w("All confidence intervals use the Wilson Score method:")
    w("")
    w("```")
    w("center = (p̂ + z²/2n) / (1 + z²/n)")
    w("spread = z × √((p̂(1-p̂) + z²/4n) / n) / (1 + z²/n)")
    w("CI = [center - spread, center + spread]")
    w("```")
    w("")
    w("where p̂ = k/n (observed proportion), z = 1.96 (95% confidence), n = sample")
    w("size, k = number of correct answers.")
    w("")
    w("Wilson Score is preferred over the normal (Wald) approximation (p̂ ± z√(p̂(1-p̂)/n))")
    w("because it provides better coverage probability, especially for proportions")
    w("near 0 or 1 and sample sizes under 100. At n=96, the difference between Wilson")
    w("and Wald is non-trivial (~0.5 percentage points in CI bounds).")
    w("")
    w("**Note on conservatism:** The CI overlap test used in the distinguishability")
    w("analysis above is stricter than a two-proportion z-test. Non-overlapping CIs")
    w("guarantee significance, but overlapping CIs do *not* guarantee non-significance.")
    w("A proper z-test finds 2 additional pairs distinguishable that the CI overlap")
    w("method calls indistinguishable (MemU vs. Mem0 on Single-hop and Multi-hop,")
    w("where the CIs overlap by <0.4 percentage points). This means the analysis")
    w("above is conservative — the true number of indistinguishable comparisons is")
    w("likely lower than reported, making the headline finding (56% indistinguishable)")
    w("an understatement if anything.")
    w("")
    w("Scores used are per-question majority vote (2-of-3 judge agreement), matching")
    w("the methodology in [RESULTS_AUDIT.md](RESULTS_AUDIT.md). Data source:")
    w("[per_category_breakdown.json](per_category_breakdown.json).")
    w("")
    w("Script: [statistical_validity.py](statistical_validity.py)")
    w("")

    return "\n".join(lines)


def main():
    data = load_data()
    report = generate_report(data)

    out_path = Path(__file__).parent / "STATISTICAL_VALIDITY.md"
    with open(out_path, "w") as f:
        f.write(report)

    print(f"Wrote {out_path}")
    print(f"  {len(report)} chars, {report.count(chr(10))} lines")


if __name__ == "__main__":
    main()
